package controller;

/**
 * A direction enum, for use by the movement button panel.
 * @author Joseph Graves
 * @version Fall 2021
 */
public enum Direction {
    /**
     * The upward direction.
     */
    UP,

    /**
     * The eastbound direction.
     */
    RIGHT,

    /**
     * The westbound direction.
     */
    DOWN,

    /**
     * The downward direction.
     */
    LEFT
}
